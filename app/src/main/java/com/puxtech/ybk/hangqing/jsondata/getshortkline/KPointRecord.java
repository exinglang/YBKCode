package com.puxtech.ybk.hangqing.jsondata.getshortkline;

import com.google.gson.annotations.Expose;
import com.google.gson.annotations.SerializedName;

/**
 * Created by fanshuo on 16/5/12.
 */
public class KPointRecord {

    @Expose
    @SerializedName("TIME")
    private long time;
    @Expose
    @SerializedName("OPEP")
    private float opep;
    @Expose
    @SerializedName("CLOP")
    private float clop;
    @Expose
    @SerializedName("HIGP")
    private float higp;
    @Expose
    @SerializedName("LOWP")
    private float lowp;
    @Expose
    @SerializedName("NVOL")
    private int nvol;
    @Expose
    @SerializedName("PRID")
    private int prid;

    public long getTime() {
        return time;
    }

    public void setTime(long time) {
        this.time = time;
    }

    public float getOpep() {
        return opep;
    }

    public void setOpep(float opep) {
        this.opep = opep;
    }

    public float getClop() {
        return clop;
    }

    public void setClop(float clop) {
        this.clop = clop;
    }

    public float getHigp() {
        return higp;
    }

    public void setHigp(float higp) {
        this.higp = higp;
    }

    public float getLowp() {
        return lowp;
    }

    public void setLowp(float lowp) {
        this.lowp = lowp;
    }

    public int getNvol() {
        return nvol;
    }

    public void setNvol(int nvol) {
        this.nvol = nvol;
    }

    public int getPrid() {
        return prid;
    }

    public void setPrid(int prid) {
        this.prid = prid;
    }
}
